package TopLaptops.DB;

public class STORAGE {
	private int nvme = 0;
	private int ssd = 0;
	private int hdd = 0;
	public int getNVMe() {
		return nvme;
	}
	public void setNVMe(int nvme) {
		this.nvme = nvme;
	}
	public int getSSD() {
		return ssd;
	}
	public void setSSD(int ssd) {
		this.ssd = ssd;
	}
	public int getHDD() {
		return hdd;
	}
	public void setHDD(int hdd) { 
		this.hdd = hdd;
	}
}
